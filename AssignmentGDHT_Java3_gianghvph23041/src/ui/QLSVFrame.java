    private SinhVien getFrom(){
        String maSv = txt_maSV.getText().trim();
        String HoTen = txt_hoTen.getText().trim();
        String email = txt_email.getText().trim();
        String Sodt = txt_sdt.getText().trim();
        String diachi = jt_diaChi.getText().trim();
        
        String hinhAnh = fileAnh.getName();
        
        
        if(maSv.length()==0||
                HoTen.length()==0||
                email.length()==0||
                Sodt.length()==0||
                diachi.length()==0){
            JOptionPane.showMessageDialog(this, "Vui lòng không để thông tin trống");
            return null;
        }
        boolean gioitinh = false;
        if(rd_nam.isSelected()){{
            gioitinh=true;
        }
        
    }
        SinhVien sv = new SinhVien(Integer.parseInt(maSv), HoTen, email, Sodt, gioitinh, diachi, hinhAnh);
        return sv;
    }
    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        SinhVien sv = this.getFrom();
        if(sv==null){
            return;
        }
        this.svSerV.insert(sv);
        this.loadData(svSerV.getlist());
        this.clearForm();
    }//GEN-LAST:event_btn_saveActionPerformed
    public void getHinhAnh(String anh) {
        ImageIcon imageicon = new ImageIcon(new ImageIcon(anh).getImage().getScaledInstance(227, 268, Image.SCALE_DEFAULT));
        lbl_anh.setText("");
        lbl_anh.setIcon(imageicon);
    }
    private void tb_qlsvMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_qlsvMouseClicked
        int row = tb_qlsv.getSelectedRow();
        txt_maSV.setText(tb_qlsv.getValueAt(row, 0).toString());
        txt_hoTen.setText(tb_qlsv.getValueAt(row, 1).toString());
        txt_email.setText(tb_qlsv.getValueAt(row, 2).toString());
        txt_sdt.setText(tb_qlsv.getValueAt(row, 3).toString());
        if (tb_qlsv.getValueAt(row, 4).toString().equals("Nam")) {
            rd_nam.setSelected(true);
        } else {
            rd_nu.setSelected(true);
        }
        jt_diaChi.setText(tb_qlsv.getValueAt(row, 5).toString());
        getHinhAnh(tb_qlsv.getValueAt(row, 6).toString());
    }//GEN-LAST:event_tb_qlsvMouseClicked

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        int row = tb_qlsv.getSelectedRow();
        if(row==-1){
            JOptionPane.showMessageDialog(this, "Chọn 1 dòng rồi xóa");
            return;
        }
        int xacnhan = JOptionPane.showConfirmDialog(this, "Xác nhận xóa?");
        if(xacnhan!=JOptionPane.YES_OPTION){
            return;
        }
        String maSv = this.tb_qlsv.getValueAt(row, 0).toString();
        this.svSerV.delete(Integer.parseInt(maSv));
        this.loadData(svSerV.getlist());
        this.clearForm();
    }//GEN-LAST:event_btn_deleteActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        int row = tb_qlsv.getSelectedRow();
        if(row==-1){
            JOptionPane.showMessageDialog(this, "Chọn 1 dòng để cập nhập");
            return;
        }
        SinhVien sv =this.getFrom();
        if(sv==null){
            return;
            }
        this.svSerV.update(sv.getMaSV(), sv);
        this.loadData(svSerV.getlist());
        this.clearForm();
    }//GEN-LAST:event_btn_updateActionPerformed

