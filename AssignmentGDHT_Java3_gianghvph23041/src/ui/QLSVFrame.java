    public void loadData(ArrayList<SinhVien> list) {
        DefaultTableModel defaultTableModel = (DefaultTableModel) tb_qlsv.getModel();
        defaultTableModel.setRowCount(0);
        for (SinhVien sv : list) {
            Object[] rowData = {
                sv.getMaSV(),sv.getHoTen(),sv.getEmail(),sv.getSoDT(),sv.hienThiGioiTinh(),sv.getDiaChi(),sv.getHinhAnh()
            };
            defaultTableModel.addRow(rowData);
        }

    }
