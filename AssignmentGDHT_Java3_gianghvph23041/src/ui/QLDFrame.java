    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        int row = tb_QLDSV.getSelectedRow();
        if(row==-1){
            JOptionPane.showMessageDialog(this, "Chọn 1 dòng để cập nhập");
            return;
        }
        
        Diem d =this.getFormData();
        if(d==null){
            return;
            }

        this.diemSV.update(d.getMaSV(), d);
        this.loadTable(diemSV.getListD());
        this.clearForm();
    }//GEN-LAST:event_btn_updateActionPerformed

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        int row = tb_QLDSV.getSelectedRow();
        if(row==-1){
            JOptionPane.showMessageDialog(this, "Chọn 1 dòng rồi xóa");
            return;
        }
        int xacnhan = JOptionPane.showConfirmDialog(this, "Xác nhận xóa?");
        if(xacnhan!=JOptionPane.YES_OPTION){
            return;
        }
        
        String maSv = this.tb_QLDSV.getValueAt(row, 0).toString();
        this.diemSV.delete(Integer.parseInt(maSv));
        this.loadTable(diemSV.getListD());
        this.clearForm();
    }//GEN-LAST:event_btn_deleteActionPerformed

