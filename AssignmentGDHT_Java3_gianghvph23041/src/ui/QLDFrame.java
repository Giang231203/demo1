    public void loadTable(ArrayList<Diem> arD){
        DefaultTableModel defaultTableModel = (DefaultTableModel) tb_QLDSV.getModel();
        defaultTableModel.setRowCount(0);
        for (Diem d :arD) {
            Object[] rowData = {
                d.getMaSV(),
                d.getHoTen(),
                d.getTiengAnh(),
                d.getTinHoc(),
                d.getGDTC(),
                d.getDiemTB()
            };
            defaultTableModel.addRow(rowData);
        }
    }
