    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        // TODO add your handling code here:
        String user = txt_user.getText().trim();
        String pass = String.valueOf(txt_password.getPassword()).trim();
        
        NewJFrame1 frame1 = new NewJFrame1();
        account = accountService.login(user, pass);

        if (user.length() == 0 || pass.length() == 0) {
            JOptionPane.showMessageDialog(this, " không đc bỏ trống !", "Message", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        if (user.equals(account.getUser()) && pass.equals(account.getPass())) {

            JOptionPane.showMessageDialog(this, " Login succesfully !", "Message", JOptionPane.INFORMATION_MESSAGE);
            frame1.setVisible(true);
            this.dispose();
            
        } else {
            JOptionPane.showMessageDialog(this, " Login Failed !", "Message", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btn_loginActionPerformed

