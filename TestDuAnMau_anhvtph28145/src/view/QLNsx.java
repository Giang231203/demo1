    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        // TODO add your handling code here:
        NSX nsx = this.getdata();
        if (nsx == null) {
            return;
        }
        JOptionPane.showMessageDialog(this, svnsx.insert(nsx));

        loadtable();
        clearform();
    }//GEN-LAST:event_btn_addActionPerformed

    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        // TODO add your handling code here:
        NSX nsx = this.getdata();
        if (nsx == null) {
            return;
        }
        int row = tb_nsx.getSelectedRow();
        String ma = tb_nsx.getValueAt(row, 0).toString();
        JOptionPane.showMessageDialog(this, svnsx.update(nsx, ma));
        loadtable();
        clearform();
    }//GEN-LAST:event_btn_editActionPerformed

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        int row = tb_nsx.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Chay chon dong de xoa");
            return;

        }
        String ma = tb_nsx.getValueAt(row, 0).toString();

        JOptionPane.showMessageDialog(this, svnsx.delete(ma));

        loadtable();
        clearform();

    }//GEN-LAST:event_btn_deleteActionPerformed

    private void tb_nsxMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_nsxMouseClicked
        // TODO add your handling code here:
        int row = tb_nsx.getSelectedRow();
        txt_mansx.setText(tb_nsx.getValueAt(row, 0).toString());
        txt_tennsx.setText(tb_nsx.getValueAt(row, 1).toString());

    }//GEN-LAST:event_tb_nsxMouseClicked
    public void clearform() {
        txt_mansx.setText("");
        txt_tennsx.setText("");
    }

