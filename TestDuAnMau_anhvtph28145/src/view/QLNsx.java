    public void addcolumn() {
        DefaultTableModel defaultTableModel = (DefaultTableModel) tb_nsx.getModel();
        ArrayList<String> listcolumn = new ArrayList<>();
        listcolumn.add("Ma");
        listcolumn.add("Ten");

        for (String string : listcolumn) {
            defaultTableModel.addColumn(string);
        }

    }

    public void loadtable() {
        DefaultTableModel defaultTableModel = (DefaultTableModel) tb_nsx.getModel();
        defaultTableModel.setRowCount(0);
        for (NSX nsx : svnsx.getall()) {
            defaultTableModel.addRow(new Object[]{
                nsx.getMa(), nsx.getTen()
            });
        }

    }

    public NSX getdata() {
        String id = "";
        String ma = txt_mansx.getText().trim().toUpperCase();
        String ten = txt_tennsx.getText().trim();
        if (ma.length() == 0 || ten.length() == 0) {
            JOptionPane.showMessageDialog(this, "Khong duoc de trong du lieu");
            return null;
        }

        NSX nsx = new NSX(id, ma, ten);
        return nsx;
    }

