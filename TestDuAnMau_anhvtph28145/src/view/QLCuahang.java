    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        Cuahang ch = this.getdata();
        if (ch == null) {
            return;
        }
        JOptionPane.showMessageDialog(this, svcuahang.insert(ch));
        loadtable();
        clearform();
    }//GEN-LAST:event_btn_themActionPerformed

    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        // TODO add your handling code here:
        Cuahang ch = this.getdata();
        if (ch == null) {
            return;
        }
        int row = tb_cuahang.getSelectedRow();
        String ma = tb_cuahang.getValueAt(row, 0).toString();
        JOptionPane.showMessageDialog(this, svcuahang.update(ch, ma));
        loadtable();
        clearform();
    }//GEN-LAST:event_btn_suaActionPerformed

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        // TODO add your handling code here:
        int row = tb_cuahang.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Chay chon dong de xoa");
            return;
        }
        String ma = tb_cuahang.getValueAt(row, 0).toString();
        JOptionPane.showMessageDialog(this, svcuahang.delete(ma)
        );

        loadtable();
        clearform();
    }//GEN-LAST:event_btn_xoaActionPerformed

    private void tb_cuahangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_cuahangMouseClicked
        // TODO add your handling code here:
        int row = tb_cuahang.getSelectedRow();
        txt_ma.setText(tb_cuahang.getValueAt(row, 0).toString());
        txt_ten.setText(tb_cuahang.getValueAt(row, 1).toString());
    }//GEN-LAST:event_tb_cuahangMouseClicked

