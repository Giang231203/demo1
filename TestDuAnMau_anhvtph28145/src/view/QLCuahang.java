    public Cuahang getdata() {
        String id = "";
        String ma = txt_ma.getText().trim().toUpperCase();
        String ten = txt_ten.getText().trim();
        String diachi = txt_diahci.getText().trim();
        String thanhpho = txt_thanhpho.getText().trim();
        String quocgia = txt_quocgia.getText().trim();
        if (ma.length() == 0 || ten.length() == 0) {
            JOptionPane.showMessageDialog(this, "Khong duoc de trong du lieu");
            return null;
        }
        Cuahang ch = new Cuahang(id, ma, ten, diachi, thanhpho, quocgia);
        return ch;
    }

    public void loadtable() {
        DefaultTableModel defaultTableModel = (DefaultTableModel) tb_cuahang.getModel();
        defaultTableModel.setRowCount(0);
        for (Cuahang cuahang : svcuahang.getall()) {
            defaultTableModel.addRow(new Object[]{
                cuahang.getMa(), cuahang.getTen(), cuahang.getDiachi(), cuahang.getThanhpho(), cuahang.getQuocgia()
            });
        }
    }

    public void clearform() {
        txt_ma.setText("");
        txt_ten.setText("");
    }

