    private void tb_BanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_BanMouseClicked
        int row = tb_Ban.getSelectedRow();
        lblID.setText(tb_Ban.getValueAt(row, 0).toString());
        txt_ten.setText(tb_Ban.getValueAt(row, 1).toString());
        txt_soThich.setText(tb_Ban.getValueAt(row, 2).toString());
        if(tb_Ban.getValueAt(row, 3).toString().equals("Nam")){
            rd_nam.setSelected(true);
        }else{
            rd_nu.setSelected(true);
        }
    }//GEN-LAST:event_tb_BanMouseClicked

    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        int row = tb_Ban.getSelectedRow();
        if(row==-1){
            JOptionPane.showMessageDialog(this, "Chon 1 dong de sua");
            return;
        }
        Ban ban = getForm();
        if(ban==null){
            return;
        }
//        String ma = tb_Ban.getValueAt(row, 0).toString();
        banSv.Update(ban,ban.getMa());
        JOptionPane.showMessageDialog(this, "THanh cong");
        loadData();
    }//GEN-LAST:event_btn_suaActionPerformed

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        int row = tb_Ban.getSelectedRow();
        if(row==-1){
            JOptionPane.showMessageDialog(this, "Chon 1 dong de xoa");
            return;
        }
        Ban ban = getForm();
        if(ban==null){
            return;
        }
        String ma = tb_Ban.getValueAt(row, 0).toString();
        banSv.delete(ban.getMa());
        JOptionPane.showMessageDialog(this, "THanh cong");
        loadData();
    }//GEN-LAST:event_btn_xoaActionPerformed

