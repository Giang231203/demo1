    public void loadData(){
        defaultTableModel = (DefaultTableModel) tb_Ban.getModel();
        defaultTableModel.setRowCount(0);
        for (Ban ban : banSv.getList()) {
            Object[] rowData = {
                ban.getMa(),ban.getTen(),ban.getSoThich(),ban.gioiTinh()
            };
            defaultTableModel.addRow(rowData);
        }
    }
    private Ban getForm(){
        String ma = lblID.getText();
        String ten = txt_ten.getText().trim();
        String sothich = txt_soThich.getText().trim();
        if(ten.length()==0||sothich.length()==0){
            JOptionPane.showMessageDialog(this, "Thong tin khong de trong");
            return null;
        }
        int gioitinh=1;
        if(rd_nam.isSelected()){
            gioitinh=0;
        }
        Ban ban1 = new Ban(ma, ten, sothich, gioitinh);
        return ban1;
    }
