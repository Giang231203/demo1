    public void loaddata(){
        defaultTableModel = (DefaultTableModel) tb_ban.getModel();
        defaultTableModel.setRowCount(0);
        for (Ban b : banSV.getList()) {
            Object[] rowData = {
                b.getMa(),b.getTen(),b.getNgaySinh(),b.getSoThich(),b.LienLac()
            };
            defaultTableModel.addRow(rowData);
        }
    }
    private Ban getForm(){
        String ma = lblMa.getText().trim();
        String en = txt_ten.getText().trim();
        String ngaysinh = txt_ngaysinh.getText().trim();
        String sothich = txt_sothich.getText().trim();
        if(en.length()==0||ngaysinh.length()==0||sothich.length()==0){
            JOptionPane.showMessageDialog(this, "THong tin khong duoc de trong");
            return null;
        }
        int trangtai = 1;
        if(chk_lienlac.isSelected()){
            trangtai=0;
        }
        Date ngaySinh = null;
        try {
            ngaySinh = Date.valueOf(ngaysinh);
        } catch (IllegalArgumentException e) {
            e.printStackTrace();
        }
        Ban ban = new Ban(ma, en, ngaySinh, sothich, trangtai);
        return ban;
    }
