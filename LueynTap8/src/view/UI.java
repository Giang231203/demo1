    private void tb_banMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_banMouseClicked
        int row = tb_ban.getSelectedRow();
        lblMa.setText(tb_ban.getValueAt(row, 0).toString());
        txt_ten.setText(tb_ban.getValueAt(row, 1).toString());
        txt_ngaysinh.setText(tb_ban.getValueAt(row, 2).toString());
        txt_sothich.setText(tb_ban.getValueAt(row, 3).toString());
        if(tb_ban.getValueAt(row, 4).toString().equals("Con")){
            chk_lienlac.setSelected(true);
        }else{
            chk_lienlac.setSelected(false);
        }
    }//GEN-LAST:event_tb_banMouseClicked

