    public void loadData(){
        defaultTableModel = (DefaultTableModel) tb_HocKy.getModel();
        defaultTableModel.setRowCount(0);
        for (HocKy hk : hkSV.getList()) {
            Object[] rowData = {
               hk.getId(),hk.getTen(),hk.getCo_so(),hk.getSo_sv_nhap_hoc()
            };
            defaultTableModel.addRow(rowData);
        }
    }
    public void clear(){
        lblID.setText("-");
        txt_SoSv.setText("");
        txt_ten.setText("");
        cb_CS.setSelectedIndex(0);
    }
    private HocKy getForm(){
        String id = lblID.getText();
        String soSV = txt_SoSv.getText().trim();
        String ten = txt_ten.getText().trim();
        String coso = cb_CS.getSelectedItem().toString();
        if(soSV.length()==0||ten.length()==0){
            JOptionPane.showMessageDialog(this, "Thong tin khong de trong");
        }
        int ID = id.equals("-")?0:Integer.parseInt(id);
        int SoSV = Integer.parseInt(soSV);
        HocKy hk1 = new HocKy(ID, ten, coso, SoSV);
        return hk1;
    }
