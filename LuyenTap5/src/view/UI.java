    private void tb_HocKyMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_HocKyMouseClicked
        int row = tb_HocKy.getSelectedRow();
        lblID.setText(tb_HocKy.getValueAt(row, 0).toString());
        txt_ten.setText(tb_HocKy.getValueAt(row, 1).toString());
        cb_CS.setSelectedItem(tb_HocKy.getValueAt(row, 2).toString());
        txt_SoSv.setText(tb_HocKy.getValueAt(row, 3).toString());
    }//GEN-LAST:event_tb_HocKyMouseClicked

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        int row = tb_HocKy.getSelectedRow();
        if(row==-1){
            JOptionPane.showMessageDialog(this, "Chon 1 dong roi xoa");
            return;
        }
        HocKy hk = getForm();
        if(hk==null){
            return;
        }
        hkSV.delete(hk.getId());
        loadData();
        
    }//GEN-LAST:event_btn_xoaActionPerformed

