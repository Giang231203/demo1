    public void addRows(ArrayList<sinhVien> list) {
        tableModel = (DefaultTableModel) tb_danhsach.getModel();
        tableModel.setRowCount(0);
        for (sinhVien sv : list) {
            tableModel.addRow(new Object[]{
                sv.getMaSv(),
                sv.getName(),
                sv.getDiemToan(),
                sv.getDiemVan(),
                sv.getDiemAnh(),
                sv.getDiemTB(),
                sv.getHocLuc()
            });
        }
    }

    public boolean checkData() {
        if (!txt_id.getText().equals("") && !txt_ten.getText().equals("")
                && !txt_toan.getText().equals("") && !txt_van.getText().equals("") && !txt_van.getText().equals("")) {
            return true;
        } else {
            return false;
        }
    }

    public void loadForm(int row) {
        txt_id.setEditable(false);
        txt_id.setText(tb_danhsach.getValueAt(row, 0).toString());
        txt_ten.setText(tb_danhsach.getValueAt(row, 1).toString());
        txt_toan.setText(tb_danhsach.getValueAt(row, 2).toString());
        txt_van.setText(tb_danhsach.getValueAt(row, 3).toString());
        txt_anh.setText(tb_danhsach.getValueAt(row, 4).toString());
        lb_dtb.setText(tb_danhsach.getValueAt(row, 5).toString());
    }

    public void loadForm(sinhVien sv) {
        txt_id.setEditable(false);
        txt_id.setText(sv.getMaSv());
        txt_ten.setText(sv.getName());
        txt_toan.setText(sv.getDiemToan() + "");
        txt_van.setText(sv.getDiemVan() + "");
        txt_anh.setText(sv.getDiemAnh() + "");
        lb_dtb.setText(sv.getDiemTB() + "");
    }

    public sinhVien createSinhVien() {
        sinhVien sv = new sinhVien();
        sv.setMaSv(txt_id.getText().trim().toUpperCase());
        sv.setName(control.uperName(txt_ten.getText().trim()));
        try {
            sv.setDiemToan(Double.parseDouble(txt_toan.getText().trim()));
        } catch (NumberFormatException e) {
            sv.setDiemToan(-1);
        }
        try {
            sv.setDiemAnh(Double.parseDouble(txt_anh.getText().trim()));
        } catch (NumberFormatException e) {
            sv.setDiemAnh(-1);
        }
        try {
            sv.setDiemVan(Double.parseDouble(txt_van.getText().trim()));
        } catch (NumberFormatException e) {
            sv.setDiemVan(-1);
        }
        return sv;
    }

    public void clearForm() {
        txt_id.setEditable(true);
        txt_id.setText("");
        txt_ten.setText("");
        txt_toan.setText("");
        txt_van.setText("");
        txt_anh.setText("");
        lb_dtb.setText("-");
    }

