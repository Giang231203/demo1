    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        if (checkData() == true) {
            if (control.addSinhVien(createSinhVien())) {
                addRows(control.getList());
                JOptionPane.showMessageDialog(rootPane, "Thêm thành công !");
                clearForm();
            } else
                JOptionPane.showMessageDialog(rootPane, "Thêm thất bại !");
        } else
            JOptionPane.showMessageDialog(rootPane, "Thêm thất bại !");
    }//GEN-LAST:event_btn_themActionPerformed

    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        // TODO add your handling code here:
        int row = tb_danhsach.getSelectedRow();
        if (row>=0) {
            String maSv = tb_danhsach.getValueAt(row, 0).toString();
            if (control.updateSinhVien(maSv, createSinhVien()) == true) {
                int choose = JOptionPane.showConfirmDialog(rootPane, "Xác Nhận Sửa ?");
                if (choose == JOptionPane.YES_OPTION) {
                    addRows(control.getList());
                    JOptionPane.showMessageDialog(rootPane, "Cập nhật thành công !");
                    clearForm();
                } else
                    JOptionPane.showMessageDialog(rootPane, "Hủy cập nhật !");
            } else 
                JOptionPane.showMessageDialog(rootPane, "Không thể cập nhật !");
        } else
            JOptionPane.showMessageDialog(rootPane, "Bạn Chưa chọn đối tượng cần sửa !");
    }//GEN-LAST:event_btn_suaActionPerformed

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        // TODO add your handling code here:
        int row = tb_danhsach.getSelectedRow();
        if (row>=0) {
            String maSv = tb_danhsach.getValueAt(row, 0).toString();
            int choose = JOptionPane.showConfirmDialog(rootPane, "Xác nhận xóa ?");
            if (choose == JOptionPane.YES_OPTION) {
                control.deleteSinhVien(maSv);
                addRows(control.getList());
                JOptionPane.showMessageDialog(rootPane, "Xóa thành công !");
                clearForm();
            } else
                JOptionPane.showMessageDialog(rootPane, "Hủy xóa !");
        } else
            JOptionPane.showMessageDialog(rootPane, "Chưa chọn đối tượng cần xóa !");
    }//GEN-LAST:event_btn_xoaActionPerformed

    private void btn_timActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_timActionPerformed
        // TODO add your handling code here:
        String maSv = JOptionPane.showInputDialog(this,"Nhập mã sinh viên","Tìm Kiếm",JOptionPane.OK_OPTION);
        if (control.searchSinhVien(maSv) != null) {
            loadForm(control.searchSinhVien(maSv));
            JOptionPane.showMessageDialog(rootPane, "Tìm thây sinh viên có mã " + maSv);
        } else
            JOptionPane.showMessageDialog(rootPane, "Không tìm thây sinh viên có mã " + maSv);
    }//GEN-LAST:event_btn_timActionPerformed

