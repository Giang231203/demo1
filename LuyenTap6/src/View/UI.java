    private void tb_QuanLyMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_QuanLyMouseClicked
        int row = tb_QuanLy.getSelectedRow();
        lblMa.setText(tb_QuanLy.getValueAt(row, 0).toString());
        txt_Ten.setText(tb_QuanLy.getValueAt(row, 1).toString());
        txt_NgaySinh.setText(tb_QuanLy.getValueAt(row, 2).toString());
        txt_SoThich.setText(tb_QuanLy.getValueAt(row, 3).toString());
        if(tb_QuanLy.getValueAt(row, 4).toString().equals("Con")){
            chk_linlac.setSelected(true);
        }else{
            chk_linlac.setSelected(false);
        }
        
    }//GEN-LAST:event_tb_QuanLyMouseClicked

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        int row = tb_QuanLy.getSelectedRow();
        if(row==-1){
            JOptionPane.showMessageDialog(this, "Chon 1 dong roi xoa");
            return;
        }
        String ma = tb_QuanLy.getValueAt(row, 0).toString();
        qlbSV.delete(ma);
        loadData();
        
    }//GEN-LAST:event_btn_xoaActionPerformed

