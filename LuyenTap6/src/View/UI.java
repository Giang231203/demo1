    public void loadData(){
        defaultTableModel = (DefaultTableModel) tb_QuanLy.getModel();
        defaultTableModel.setRowCount(0);
        for (QuanLyBan qlb : qlbSV.getList()) {
            Object[] rowData = {
                qlb.getMa(),qlb.getTen(),qlb.getNgaySinh(),qlb.getSoThich(),qlb.ConLienLac()
            };
            defaultTableModel.addRow(rowData);
        }
    }
    private QuanLyBan getForm(){
        String ma = lblMa.getText();
        String ten = txt_Ten.getText().trim();
        String ngaysinh = txt_NgaySinh.getText().trim();
        String sothich = txt_SoThich.getText().trim();
        if(ten.length()==0||ngaysinh.length()==0||sothich.length()==0){
            JOptionPane.showMessageDialog(this, "Thong tin khong de trong");
            return null;
        }
        int lienhe = 1;
        if(chk_linlac.isSelected()){
            lienhe=0;
        }
        Date ngaySinh = null;
        try {
            ngaySinh = Date.valueOf(ngaysinh);
        } catch (IllegalArgumentException e) {
            e.printStackTrace();
        }
        QuanLyBan qlb1 = new QuanLyBan(ma, ten, ngaySinh, sothich, lienhe);
        return qlb1;
    }
